    public function productoCategoria($id){
        $array = [];
        $productos = DB::table('categorias')->join('productos',  'categorias.id', 'productos.categoriaId')->get();
        foreach($productos as $pro){
            if($id == $pro->categoriaId){
                array_push($array, $pro);
            }
        }
        echo json_encode($array );
    }
